// 非空整数组，一个数字只出现一次，其余均出现两次，找出只出现了一次的那个数字
// 不使用额外空间

// 异或运算符 ^ 遵从交换律
// n ^ n === 0
// n ^ 0 === n
// 如果数组中没有那个数字的相同数字的话 会保留下来
// [1, 2, 4, 1, 2]
// 1 ^ 2 ^ 4 ^ 1 ^ 2
// 1 ^ 1 ^ 2 ^ 2 ^ 4
//     0 ^ 2 ^ 2 ^ 4
//         2 ^ 2 ^ 4
//             0 ^ 4
//                 4


var singleNumber = function (nums) {
  let res = nums[0];
  for (var i = 1; i < nums.length; i++) {
    res = res ^ nums[i];
  }
  return res;
};